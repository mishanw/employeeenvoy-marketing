// Helper functions
export function getIntegrationsByCategory(category: IntegrationCategory): Integration[] {
  return INTEGRATIONS
    .filter(i => i.category === category)
    .sort((a, b) => b.popularity - a.popularity);
}

export function getIntegrationsByTier(tier: IntegrationTier): Integration[] {
  return INTEGRATIONS
    .filter(i => i.tier === tier)
    .sort((a, b) => b.popularity - a.popularity);
}

export function searchIntegrations(query: string): Integration[] {
  const lowerQuery = query.toLowerCase();
  return INTEGRATIONS.filter(i =>
    i.name.toLowerCase().includes(lowerQuery) ||
    i.description.toLowerCase().includes(lowerQuery)
  );
}

export function getCategoriesWithCounts(): Array<{ category: IntegrationCategory; count: number; label: string }> {
  const categories = Object.keys(CATEGORY_LABELS) as IntegrationCategory[];
  return categories.map(category => ({
    category,
    count: INTEGRATIONS.filter(i => i.category === category).length,
    label: CATEGORY_LABELS[category],
  })).filter(c => c.count > 0);
}

export function getTotalCount(): number {
  return INTEGRATIONS.length;
}

export function getScimCount(): number {
  return INTEGRATIONS.filter(i => i.scimSupport).length;
}

export function getSsoCount(): number {
  return INTEGRATIONS.filter(i => i.ssoSupport).length;
}
