      <h2>6. Managing Cookies</h2>

      <h3>6.1 Browser Settings</h3>
      <p>
        Most web browsers allow you to control cookies through their settings.
        You can typically:
      </p>
      <ul>
        <li>View what cookies are stored</li>
        <li>Delete some or all cookies</li>
        <li>Block all or certain cookies</li>
        <li>Set preferences for certain websites</li>
      </ul>

      <h3>6.2 Browser-Specific Instructions</h3>
      <p>Here's how to manage cookies in common browsers:</p>
      <ul>
        <li>
          <strong>Chrome:</strong> Settings → Privacy and Security → Cookies and
          other site data
        </li>
        <li>
          <strong>Firefox:</strong> Settings → Privacy & Security → Cookies and Site
          Data
        </li>
        <li>
          <strong>Safari:</strong> Preferences → Privacy → Manage Website Data
        </li>
        <li>
          <strong>Edge:</strong> Settings → Cookies and site permissions → Manage
          and delete cookies
        </li>
      </ul>
      <p>
        For detailed instructions, please visit your browser's help
        documentation.
      </p>

